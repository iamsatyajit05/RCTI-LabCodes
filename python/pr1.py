# Steps to download Python

# 1. Go to python.org and download the executable installer
# 2. Run the executable installer
# 3. Check the "Installer Launcher..." & "Add Python 3.x to PATH" checkbox
# 4. Select Customize Installation
# 5. Check all the options and click NEXT
# 6. Select the path where python should be downloaded and click on Install
# 7. Congratulations, Python is Installed