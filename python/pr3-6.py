# Write a program to calculate area and voume of sphere

# Area = 4 * PI * (Radius * Radius)
# Volume = (4/3) * PI * (Radius * Radius * Radius)

print("Playing with footballllll :)\n")

radius = float(input("Enter the radius of sphere: "))

area = 4 * 3.14 * (radius ** 2)
volume = (4/3) * 3.14 * (radius ** 3)

print("\nHere you go,")
print(f"Area of circle is {area} unit square")
print(f"Volume of circle is {volume} unit cube")